                    handler(newVal) {
                        this.num(newVal)
                    }
                },
                hour: {
                    handler(newVal) {
                        this.num(newVal)
                    }
                }
            },
            computed: {
                // 初始化数据
                second() {
                    return this.num(this.seconds)
                },
