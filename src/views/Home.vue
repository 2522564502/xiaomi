                num(n) {
                    return n < 10 ? '0' + n : '' + n
                },
                // 倒计时函数
                add() {
                    let time = window.setInterval( ()=> {
                        if (this.hours !== 0 && this.minutes === 0 && this.seconds === 0) {
                            this.hours -= 1;
                            this.minutes = 59;
                            this.seconds = 59;
                        } else if (this.hours === 0 && this.minutes !== 0 && this.seconds ===
                            0) {
                            this.minutes -= 1;
                            this.seconds = 59;
                        } else if (this.hours === 0 && this.minutes === 0 && this.seconds ===
                            0) {
                            this.seconds = 0
                            window.clearInterval(time)
                        } else if (this.hours !== 0 && this.minutes !== 0 && this.seconds ===
                            0) {
                            this.minutes -= 1;
                            this.seconds = 59;
                        } else {
                            this.seconds -= 1;
                        }
                    }, 1000)
                }
            },
            watch: {
                // 监听数值变化
                second: {
                    handler(newVal) {
                        this.num(newVal)
                    }
                },
